package week1.day2;

public class NegativetoPositive {

	public static void main(String[] args) {
		int number=-56;
		
	      
		if(number<0)
			
		{
			int num=-(number);
			
			System.out.println(number+ "\tNegative number is converted to\t"  +num);
					
		}
		else
		{
			System.out.println(number+"\tis a positive number");
		
	
		}
		

	}

}
